print("El perimetro del cuadrado es: ", perimetro_cuadrado(x));



def area_rectangulo (x,y):
    return(x*y);
def perimetro_rectangulo(x,y):
    return (2*x+2*y);

x = int(input(" Ingrese el primer numero "))
y = int(input(" Ingrese el segundo numero "))

print("El area del cuadrado es: ", area_rectangulo(x,y));
print("El perimetro del cuadrado es: ", perimetro_rectangulo(x,y));




